# Вхідний рядок
s = "abcdefg123"

# Гарантуємо довжину 10 символів (якщо коротший - доповнюємо, якщо довший - обрізаємо)
padded_s = s.ljust(10)[:10]  # Доповнюємо пробілами праворуч до 10 символів

# Реверсуємо рядок
reversed_s = padded_s[::-1]

# Виводимо результат
print(reversed_s)  # Завжди буде 10 символів